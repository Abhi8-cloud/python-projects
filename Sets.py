""" Sets are used to store multiple items in a single variable.
A set is a collection which is unordered, unchangeable*, and unindexed.
"""
# create a set
set = {"abhi","virat","kohli"}
print(set)

# Duplicates Not Allowed
set = {"apple","banana","apple","banana"}
print(set)

a = {"apple","banana",True,1,False,0} # True and 1 /False and 0 is considered the same value:
print(a)

# Length of a Set
b = {"abhi","virat","kohli"}
print(len(b))


